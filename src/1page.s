RAM	EQU	0800H
STACK	EQU	RAM+0000H

	ORG 0

RUN:
	LXI	H,STACK
	SPHL
	LXI 	H,HEAD
	CALL 	OUTPUT
	HLT

OUTPUT:	
OUTLOOP:
	MOV 	A,M
	CPI 	24H			; $ symbol
	JZ 	OUTEND
	OUT 	1
	INX 	H
	JMP 	OUTLOOP
OUTEND:	RET
	

HEAD:	DB 	'P3 '
	DB	'3 2 '
	DB	'255 '
	DB	'255 0 0 '
	DB	'0 255 0 '
	DB	'0 0 255 '
	DB	'255 255 0 '
	DB	'255 255 255 '
	DB	'0 0 0 '
	DB	'$'
