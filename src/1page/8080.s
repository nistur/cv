WIDTH   EQU     80
HEIGHT	EQU	25
RAM	EQU	0800H
STACK	EQU	RAM+0000H
FB	EQU	8000H
	ORG 0

RUN:
	LXI	H,STACK
	SPHL

	CALL 	OUTTEST2
	HLT

OUTTEST2:
	MVI	B,200
LOOP2:	LXI	H,MSG
	CALL	OUTPUT
	DCR 	B
	JNZ	LOOP2
	RET

OUTTEST:	
	MVI	B,WIDTH
	MVI	C,HEIGHT

LOOP:	MVI	A,031h
	OUT	1
	DCR 	B
	JNZ 	LOOP
	MVI	B,WIDTH
	MVI	A,0Ah
	OUT	1
	MVI	A,0Dh
	OUT	1
	DCR	C
	JNZ	LOOP
	
	RET

OUTPUT:
OUTLOOP:MOV	A,M
	CPI	24h
	JZ	OUTEND
	OUT	1
	INX	H
	JMP	OUTLOOP
OUTEND:	RET

MSG:	DB	'WOW 80 COLUMNS!$'
	
