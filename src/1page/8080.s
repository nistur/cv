WIDTH   EQU     80
HEIGHT	EQU	25
RAM	EQU	0800H
STACK	EQU	RAM+0000H
FB	EQU	8000H

	ORG 0

	JMP RUN

	INCLUDE	OUTPUT.S
	INCLUDE RANDOM.S
RUN:
	LXI	H,STACK
	SPHL
	CALL	TEST
	CALL	OUTPUT
	HLT

TEST:	PUSH PSW
	PUSH B
	PUSH H

	LXI	H,FB
	MVI	B,WIDTH
	MVI	C,HEIGHT

TLOOP1:
	CALL	RANDOM
	MOV	M,A
	INX	H
	DCR	B
	JNZ	TLOOP1

	MVI	B,WIDTH
	DCR	C
	JNZ	TLOOP1


	POP H
	POP B
	POP PSW

	RET
	
